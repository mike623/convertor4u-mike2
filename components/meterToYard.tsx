"use client";
export const meterToYard = (meter: number) => meter * 1.0936;
export const meterToInch = (meter: number) => meter * 39.37;
export const meterToFoot = (meter: number) => meter * 3.2808;
export const yardToMeter = (yard: number) => yard / 1.0936;
export const inchToMeter = (inch: number) => inch / 39.37;
export const footToMeter = (foot: number) => foot / 3.2808;
